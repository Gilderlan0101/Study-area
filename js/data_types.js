// Tipos de dados em java script

// Podemos colocar qualque tipo em uma variável. 
// Ela inicia sendo uma string e, em seguida, amazenar
// um numero.
let message = "Hello";
message = 123456
console.log(message)

// Números inteiros e flutuantes
let number = 1234;
console.log(typeof(number), number,  "<- INTEIRO")
// Update valor da variável number
number = 12.342;
console.log(typeof(number), number,  "<- FLOAT")


// Comparações estritas (User sempre)
console.log("5" === 5)
console.log("5" == 5) // True (evite ==)
